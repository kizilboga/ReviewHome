package com.neotech.review04;

public class Ensar {

	// no return type, no parameters
	void add() {
		int result = 3 + 4;
	}

	// no return type, 2 parameters
	void addNumb(int a, int b) {
		int result = a + b;

	}

	// int return type, 2 parameters
	int addGive(int a, int b) {
		int result = a + b;
		return result;
	}

	// int return type, no parameters
	int noGive() {
		int result = 7 + 3;
		return result;

	}
}
